#include "../../../includes/asm.h"

void			trim_str(char *str, int *i)
{
	while (str[*i] == '\t' || str[*i] == ' ' ||
			(str[*i] >= 9 && str[*i] <= 13))
	{
		*i += 1;
	}
}
